namespace InkSim
{
    // The "axis of reality" an ink can affect. Carrier controls delivery; domain controls meaning.
    public enum InkDomain
    {
        Binding,   // sealing, permanence, constraint
        Growth,    // life, mutation, ecology
        Memory,    // mind, recording, identity traces
        Decay,     // corrosion, rot, entropy
        Energy     // heat, impulse, activation
    }
}
